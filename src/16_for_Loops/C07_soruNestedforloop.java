package day16_for_Loops;

public class C07_soruNestedforloop {

	public static void main(String[] args) {
		
		
		for (int satir = 1; satir <=4; satir++) {
			for (int i = 1; i <=satir; i++) {
				System.out.print("*");
			}
			System.out.println();
		}
		
		
	
		
		
		
		
		
		
		
		
		
		
		
	}

}
