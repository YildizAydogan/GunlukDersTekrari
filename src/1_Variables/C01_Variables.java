package day01_Variables;

public class C01_Variables {

	public static void main(String[] args) {
		// Variable olusturmak ve deger atamak farkli islemlerdir. Istersek her ikisini ayni satirda yapabilirz.
		String ogrenciIsim="Yildiz";
		System.out.println(ogrenciIsim);
		
		
		
		
	  //Istersekte once Variable olusturur, istedigimiz zamanda deger atariz.
		int sayi1;
		sayi1=10;
		System.out.println(sayi1);
		
		
		
		
		//Daha once olusturulmus ve deger atamasi yapilmis bir degiskene yeni deger atayabilirz.
		//Bu durumda java variablenin eski degerini silip yeni degeri variable a atar.
		sayi1=25;
		System.out.println(sayi1);
		ogrenciIsim="Fatih";
		System.out.println(ogrenciIsim);	
		
		
		
		
		
		
		
		
		
		

	}

}
