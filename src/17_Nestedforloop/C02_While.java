package day17_Nestedforloop;

public class C02_While {

	public static void main(String[] args) {
		//20 den 30 a kadarki sayilari whileLoop ile yazdirin
		
		for (int i = 20; i <=30; i++) {
			System.out.print(i+" ");
		}
		System.out.println("");
		
		int sayi=20;
		while (sayi<=30) {
			System.out.print(sayi+ " ");
			sayi++;
		}
		
		
		//forloop ile cozebildigimiz sorularda
		//whilellop kullanmak kodumuzu biraz uzatir

	}

}
